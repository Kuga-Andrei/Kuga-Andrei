''' This library has funtions for A8_T2
add,
subtract,
multiply,
divide
'''

def add(PAddend1: float, PAddend2: float) -> float:
    result = PAddend1 + PAddend2
    return result

def subtract(PMinuend: float, PSubtrahend: float) -> float:
    result = PMinuend - PSubtrahend
    return result

def multiply(PMultiplicant: float, PMultiplier: float) -> float:
    result = PMultiplicant * PMultiplier
    return result

def divide(PDividend: float, PDivisor: float) -> float:
    result = PDividend / PDivisor
    return result
